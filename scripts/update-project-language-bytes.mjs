import fs from 'node:fs/promises';
import path from 'node:path';

function extractGitHubProjects(projectsTs) {
	const projects = [];
	const segmentRegex = /slug:\s*'([^']+)'([\s\S]*?)(?=\n\s*slug:\s*'|\n\];)/g;

	for (const match of projectsTs.matchAll(segmentRegex)) {
		const slug = match[1];
		const segmentBody = match[2] ?? '';
		const githubMatch = segmentBody.match(/github:\s*'https:\/\/github\.com\/([^'\s]+)'/);
		if (!githubMatch) continue;

		const repoPath = githubMatch[1].replace(/\.git$/, '').replace(/\/$/, '');
		const [owner, repo] = repoPath.split('/');
		if (!owner || !repo) continue;

		projects.push({ slug, owner, repo });
	}

	return projects;
}

async function fetchLanguageBytes(owner, repo, token) {
	const res = await fetch(`https://api.github.com/repos/${owner}/${repo}/languages`, {
		headers: {
			Accept: 'application/vnd.github+json',
			'User-Agent': 'portfolioSite',
			...(token ? { Authorization: `Bearer ${token}` } : {})
		}
	});

	if (!res.ok) {
		let msg = '';
		try {
			const body = await res.json();
			msg = body?.message ? `: ${body.message}` : '';
		} catch {
			msg = '';
		}
		throw new Error(`GitHub languages failed for ${owner}/${repo} (${res.status})${msg}`);
	}

	const data = await res.json();
	const values = Object.values(data).filter((v) => Number.isFinite(v));
	return values.reduce((sum, n) => sum + Number(n), 0);
}

async function main() {
	const repoRoot = process.cwd();
	const projectsPath = path.join(repoRoot, 'src/lib/data/projects.ts');
	const outPath = path.join(repoRoot, 'src/lib/data/projectLanguageBytes.ts');
	const token = process.env.GH_TOKEN || process.env.GITHUB_TOKEN;

	const projectsTs = await fs.readFile(projectsPath, 'utf8');
	const ghProjects = extractGitHubProjects(projectsTs);

	if (ghProjects.length === 0) {
		throw new Error('No GitHub projects found in src/lib/data/projects.ts');
	}

	const byteMap = {};
	for (const project of ghProjects) {
		try {
			const bytes = await fetchLanguageBytes(project.owner, project.repo, token);
			byteMap[project.slug] = bytes;
		} catch (error) {
			console.warn(error instanceof Error ? error.message : String(error));
			byteMap[project.slug] = 0;
		}
	}

	const sortedEntries = Object.entries(byteMap).sort(([a], [b]) => a.localeCompare(b));
	const lines = sortedEntries.map(([slug, bytes]) => `\t'${slug}': ${bytes}`).join(',\n');
	const generatedAt = new Date().toISOString();

	const output = `// Generated by scripts/update-project-language-bytes.mjs\n// Last updated: ${generatedAt}\n\nexport const projectLanguageBytes: Record<string, number> = {\n${lines}\n};\n`;

	await fs.writeFile(outPath, output, 'utf8');
	console.log(`Wrote ${sortedEntries.length} entries to ${path.relative(repoRoot, outPath)}`);
}

main().catch((error) => {
	console.error(error instanceof Error ? error.message : String(error));
	process.exit(1);
});
